package Глава7_Рассмотрение_классов_и_методов.P205_Array;

public class Length {
    public static void main(String[] args) {
        int a1[] = new int[12];
        int a2[] = {3,5,7,1,8,99,44,-11};
        int a3[] = {4,3,2,1};

        System.out.println("Длина a1 равна" + a1.length);
        System.out.println("Длина a2 равна" + a2.length);
        System.out.println("Длина a3 равна" + a3.length);

    }
}
